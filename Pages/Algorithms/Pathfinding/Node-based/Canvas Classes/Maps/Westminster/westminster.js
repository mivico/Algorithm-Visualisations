function mapWestminster() {
    canvas.style.backgroundColor = 'rgba(0, 0, 0, 0.03)';
    initialiseWestminsterVertices();
    initialiseWestminsterEdges();
    initialiseWestminsterTriangles();
    //RENDER_IMAGE_SRC = './Canvas Classes/Maps/Westminster/img.png';

    for (let i = 0; i < WESTMINSTER_VERTICES.length; i++) {
        const tempCircle = new Circle(WESTMINSTER_VERTICES[i].x, WESTMINSTER_VERTICES[i].y)
        const temp = new HeapNode(Infinity, tempCircle)
        temp.index = GRAPH_ADJACENCY_LIST.createVertex(temp).index;
    }

    for (let i = 0; i < WESTMINSTER_EDGES.length; i++) {
        const fromIndex = WESTMINSTER_EDGES[i].from;
        const toIndex = WESTMINSTER_EDGES[i].to;
        const weight = WESTMINSTER_EDGES[i].weight * calculateDistance(GRAPH_ADJACENCY_LIST.adjacencyList[fromIndex].vertex.node, GRAPH_ADJACENCY_LIST.adjacencyList[toIndex].vertex.node);
        const tempEdge = new Line(GRAPH_ADJACENCY_LIST.adjacencyList[fromIndex].vertex.node, GRAPH_ADJACENCY_LIST.adjacencyList[toIndex].vertex.node, weight);
        GRAPH_ADJACENCY_LIST.addUndirectedEdge(GRAPH_ADJACENCY_LIST.adjacencyList[fromIndex].vertex, GRAPH_ADJACENCY_LIST.adjacencyList[toIndex].vertex, weight, tempEdge);
    }

    for (let i = 0; i < WESTMINSTER_TRIANGLES.length; i++) {
        const a = WESTMINSTER_TRIANGLES[i].a;
        const b = WESTMINSTER_TRIANGLES[i].b;
        const c = WESTMINSTER_TRIANGLES[i].c;
        const item = WESTMINSTER_TRIANGLES[i].item;
        const tempTri = new Triangle(GRAPH_ADJACENCY_LIST.adjacencyList[a].vertex.node, GRAPH_ADJACENCY_LIST.adjacencyList[b].vertex.node, GRAPH_ADJACENCY_LIST.adjacencyList[c].vertex.node, item);
        GRAPH_ADJACENCY_LIST.addTriangle(tempTri);
    }
}

